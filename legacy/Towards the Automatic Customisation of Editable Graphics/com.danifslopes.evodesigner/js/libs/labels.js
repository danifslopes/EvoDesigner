function callConceptNet(tx1, tx2) {
    $.writeln(tx1 + ", " + tx2);
    var url = 'http://beatrix.dei.uc.pt:8080/relatedness?node1=/c/en/' + tx1 + '&node2=/c/en/' + tx2;
    var v = loadJSON(url).value;
    return v;
}

function getLabelScores(label) {
    var allScores = {
        all: []
    };
    if (interfaceSettings.keywords.length > 0) {
        for (var k = 0; k < interfaceSettings.keywords.length; k++) {
            var sim = callConceptNet(label, interfaceSettings.keywords[k]);
            allScores[k] = sim;
            allScores.all.push(sim)
        }
    } else allScores.all.push(itemMutationProb); //se nao ha keywords,prob default

    return allScores;
}

